public class Opincre
{
    public static void main(String[] args)
    {
      int i = 0;
      int y = 0;

      //++i; Op. pre-incremento
      System.out.println(++i);
      System.out.println(y++);
      System.out.println(y);

      System.out.println("<-------------->");
      int a = 0;
      int b = 0;

      System.out.println(--a);
      System.out.println(b--);
      System.out.println(b);

    }
}
